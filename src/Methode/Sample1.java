package Methode;

public class Sample1 {//static method call from same class 
	
public static void main(String[] args) {// main method body is open
	m1();
	m2();
	m3 ();
	
	
}//main method body is close
public static void m1() {//m1 block is open
	System.out.println("hii...");
	System.out.println("hello");
	System.out.println("now u r program is run");
}//m1 block is close

public static void m2() {//m2 block is open
	System.out.println("we print soemthing here");
	System.out.println("what is that");
	System.out.println("Durga mat ki jay");
	
} //m2 block is close

public static void m3 () {// m3 block is open
	System.out.println("which methode u r created");
	System.out.println("i am creating regular methode");
	System.out.println("how many tupe of regular methode");
	System.out.println("two type of regular methode");
	
	
	
}//m3 block is close
}//class body is close
