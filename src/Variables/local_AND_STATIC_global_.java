package Variables;

public class local_AND_STATIC_global_ {//class body open
	 static int j=20;//it is outside the main method body hence it is called as global variable
	
public static void main(String[] args) {//main method body open
	int i;//variable declaration
	i=25;//variable initialization
	
	System.out.println(i);
	System.out.println(j);
	
	
}
}//class body close
